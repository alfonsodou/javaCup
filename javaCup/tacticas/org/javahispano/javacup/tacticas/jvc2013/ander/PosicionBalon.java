package org.javahispano.javacup.tacticas.jvc2013.ander;

public class PosicionBalon {

	/**
	 * @param args
	 */
	public double X;
	public double altura;
	public double deltaX;
	public double deltaAltura;
	public PosicionBalon(double pX,double pAltura,double pDeltaX,double pDeltaAltura){
		X=pX;
		altura=pAltura;
		deltaX=pDeltaX;
		deltaAltura=pDeltaAltura; 
	}
	

}
